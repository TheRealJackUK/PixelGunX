	public static string TagOfFirstUpgrade(string tg)
	{
		if (tg == null)
		{
			return null;
		}
		foreach (List<string> upgrade in upgrades)
		{
			if (upgrade.Contains(tg))
			{
				return upgrade[0];
			}
		}
		return tg;
	}

	public static List<string> ChainForTag(string tg)
	{
		if (tg == null)
		{
			return null;
		}
		foreach (List<string> upgrade in upgrades)
		{
			if (upgrade.Contains(tg))
			{
				return upgrade;
			}
		}
		return null;
	}
